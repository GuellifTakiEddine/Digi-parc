    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
      int selectedRowIndex = jTable2.getSelectedRow();
    
    // If a row is selected
    if (selectedRowIndex != -1) {
        // Get the value of the ID column from the selected row
        int id = (int) jTable2.getValueAt(selectedRowIndex, 0);

        // Show a confirmation dialog to confirm the removal
        int option = JOptionPane.showConfirmDialog(this, "Are you sure you want to remove this employee?", "Confirmation", JOptionPane.YES_NO_OPTION);
        
        // Check the user's choice
        if (option == JOptionPane.YES_OPTION) {
            // Execute a SQL DELETE statement to remove the row with the selected ID from the database
            String sql = "DELETE FROM employe WHERE IdEmp = ?";
            try (

                    Connection conn = DriverManager.getConnection(url);
                
                PreparedStatement stmt = conn.prepareStatement(sql);
            ) {
                
                stmt.setInt(1, id);
               
                int rowsAffected = stmt.executeUpdate();

               
                if (rowsAffected > 0) {
                    
                    DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
                    model.removeRow(selectedRowIndex);
                    
                    Notifications.getInstance().show(Notifications.Type.INFO, Notifications.Location.TOP_CENTER, "Employee removed successfully!");

                } else {
                    
                    JOptionPane.showMessageDialog(this, "Failed to remove employee.", "Error", JOptionPane.ERROR_MESSAGE);
                }
            } catch (SQLException ex) {
                ex.printStackTrace();
                
                JOptionPane.showMessageDialog(this, "An error occurred while accessing the database.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    } else {
        // Inform the user that no row is selected
        JOptionPane.showMessageDialog(this, "Please select a row to remove.", "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_jButton1ActionPerformed

