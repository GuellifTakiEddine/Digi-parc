    public void getTablecontent(){
       String sql = "SELECT * FROM employe";

        try (
            // Establish the database connection
            Connection conn = DriverManager.getConnection(url);
            // Create a PreparedStatement object for the query
            PreparedStatement stmt = conn.prepareStatement(sql);
            // Execute the query and get the ResultSet
            ResultSet tt = stmt.executeQuery()
        ) {
            // Create a DefaultTableModel to hold the data for the JTable
            DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
            // Clear any existing data from the table
            model.setRowCount(0);

            // Iterate through the ResultSet and add each row to the table model
            while (tt.next()) {
                Object[] rowData = {
                    tt.getInt("IdEmp"),
                    tt.getString("NomEmp"),
                    tt.getString("PrenomEmp"),tt.getString("Date_Naissance_Emp"),tt.getString("Date_Embauche_Emp"),tt.getString("email"),tt.getString("phonenumber"),
                    tt.getString("username"),tt.getString("password")
                    
                };
                model.addRow(rowData);
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
            // Handle any exceptions that occur during database access
        }
        

}

   
